
public class Person {
	private String name;
	private String dateOfBirth;
	private String gender;
	private String mobileNumber;
	private String bloodGroup;
	
	// name getter and setter
	public String getName() {
		return name;
	}
	public void setName(String newName) {
		this.name = newName;
	}
	
	// date of birth getter and setter
	public String getDateOfBirth() {
		return dateOfBirth;
	}
	public void setDateOfBirth(String newDateOfBirth) {
		this.dateOfBirth = newDateOfBirth;
	}
	
	// gender getter and setter
	public String getGender() {
		return gender;
	}
	public void setGender(String newGender) {
		this.gender = newGender;
	}
	
	// mobile number getter and setter
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String newMobileNumber) {
		this.mobileNumber = newMobileNumber;
	}
	
	// blood group getter and setter
	public String getBloodGroup() {
		return bloodGroup;
	}
	public void setBloodGroup(String newBloodGroup) {
		this.bloodGroup = newBloodGroup;
	}
}
